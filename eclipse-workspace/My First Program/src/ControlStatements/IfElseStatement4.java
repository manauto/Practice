package ControlStatements;

public class IfElseStatement4 {
	
	public static void main(String[] args) {
		// check whether a number is odd or even
		
		int number = 555;
		//555%2=0
		
		if (number%2 == 0) {
			System.out.println("The number is even");
		}
		else {
			System.out.println("The number is odd");
			
	}
		
	}
	
	
}
