package ControlStatements;

public class SwitchStatement2 {
	
	public static void main(String [] args) {
	
	char ch ='R';
	
	switch(ch) {
	case 'A':System.out.println("Matched A");
	case 'B':System.out.println("Matched B");
	case 'C':System.out.println("Matched C");
	break;
	default:System.out.println("No Matched");
		
	
	}
	}
}

