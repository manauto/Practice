package ControlStatements;

public class Swtichstatement1 {
	
	public static void main(String []args)   {
	
		int number = 200;
		switch (number) {
		//case statements
		case 10:
			System.out.println("Number added-10");
		case 20:
			System.out.println("Number added-20");
		case 100:
			System.out.println("Number added-100");
		case 200:
			System.out.println("Number added-200");
		break;
	    default:
			System.out.println("Number is not matched");
		}
        }
        }
