package LoopStatement;

public class WhileLoop7 {

public static void main (String [] args ) {
	
	
	//Write a program to add numbers from 1 to 50
	
	int d=1;
    int sum=0;
    
    while(d<=50) {
    	
    	sum=sum+d;
    	d++;
    }

    	System.out.println("addition of numbers from 1 to 50:"+ sum);
    	
    	
    }
}
